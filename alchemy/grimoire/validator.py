def validate_ingredients(ingredients: str) -> str:
    if ingredients == "fire":
        return f"{ingredients} - VALID"
    elif ingredients == "water":
        return f"{ingredients} - VALID"
    elif ingredients == "earth":
        return f"{ingredients} - VALID"
    elif ingredients == "air":
        return f"{ingredients} - VALID"
    return f"{ingredients} - INVALID"
